    echo "  2. Set up your VPS with Dokploy"
    echo "  3. Follow docs/deployment/DEPLOYMENT_INSTRUCTIONS.md"
    echo "  4. Test with: ./scripts/test_deployment.sh <your-server-url> <api-key>"
    echo
